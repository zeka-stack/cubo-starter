package dev.dong4j.zeka.starter.rest.support;

/**
 * 子类类型接口
 *
 * 该接口用于定义可以获取子类类型信息的组件。
 * 主要用于抽象类或接口在运行时需要获取具体实现类的场景。
 *
 * 使用场景：
 * 1. 抽象表单参数解析：根据类型标识选择对应的子类
 * 2. 多态对象序列化：根据类型信息选择正确的反序列化策略
 * 3. 策略模式实现：根据类型选择不同的处理策略
 * 4. 工厂模式实现：动态创建不同类型的对象
 *
 * 实现方式：
 * 通常由某个枚举类型实现，每个枚举值对应一个具体的子类。
 * 这样可以在运行时根据枚举值快速定位到对应的子类类型。
 *
 * @author dong4j
 * @version 1.0.0
 * @email "mailto:dong4j@gmail.com"
 * @date 2020.05.23 19:15
 * @since 1.0.0
 */
public interface SubClassType {

    /**
     * 获取对应的子类类型
     *
     * 该方法返回与当前实例对应的子类 Class 对象。
     * 通常用于反射创建对象、类型检查或序列化操作。
     *
     * 实现要求：
     * - 返回的 Class 对象必须是具体的类，不能是抽象类或接口
     * - 返回的类必须具有可访问的构造方法
     * - 对于同一个实例，多次调用应该返回相同的结果
     *
     * @return 对应的子类 Class 对象，不能为 null
     * @since 1.0.0
     */
    Class<?> getSubClass();
}
